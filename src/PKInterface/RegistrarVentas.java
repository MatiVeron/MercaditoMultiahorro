    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String codigo =jTextFieldCodigo.getText();
        String nombre =jTextFieldNombre.getText();
        String precio=jTextFieldPrecio.getText();
        String cant=jTextFieldCant.getText();
        
        DefaultTableModel modelo = (DefaultTableModel) jTableVenta.getModel() ;
        modelo.addRow(new Object[]{codigo,nombre,precio,cant});
        
        int cantidad = Integer.parseInt( jTextFieldCant.getText());
           
        if(cantidad > 0){
            try{
           
            
                Connection con = ConexionBD.getConexion();
                String sql = "UPDATE productos SET cantidad = cantidad - "+cantidad+" WHERE id_producto ="+codigo+" ";
                
                PreparedStatement sent = con.prepareStatement(sql);
               
                
                sent.executeUpdate();
                
                con.close();
                
                
                
                TablaProductos tabla = new TablaProductos();
                tabla.LlenarTabla(jTableProductos);
                this.limpiarTexto();
        
            
            }catch(Exception e){
            
                JOptionPane.showMessageDialog(null, e, "error en BD",JOptionPane.ERROR_MESSAGE);
        
        
            }}
        
       
        
    }//GEN-LAST:event_jButton1ActionPerformed

