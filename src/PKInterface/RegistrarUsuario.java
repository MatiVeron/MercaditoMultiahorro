    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Usuarios u = new Usuarios();
        u.setUsuario(jTextField1.getText());
        u.setPassword(jPasswordField1.getText());
        u.setRepeatPassword(jPasswordField2.getText());
        
        if(u.getUsuario().isEmpty()|| u.getPassword().isEmpty()||u.getRepeatPassword().isEmpty()){
            JOptionPane.showMessageDialog(null,"Algun campo no ha sido insertado");
        }else{
            if(u.getPassword().contentEquals(u.getRepeatPassword()))
            {  
                u.insertUsuario(u.getUsuario(), u.getPassword());
                this.dispose();
            }else
            {
            JOptionPane.showMessageDialog(null,"Las Contraseñas no coinciden");
            }
        
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

